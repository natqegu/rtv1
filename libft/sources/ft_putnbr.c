#include "libft.h"

void	ft_putnbr(int n)
{
	if (n < 0)
	{
		ft_putstr("-");
		n = -n;
	}
	if (-n == -2147483648)
	{
		ft_putstr("2");
		n -= 2000000000;
	}
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	else
		ft_putchar(n + '0');
}
