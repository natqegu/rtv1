#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n < 0)
	{
		ft_putstr_fd("-", fd);
		n = -n;
	}
	if (-n == -2147483648)
	{
		n -= 2000000000;
		ft_putstr_fd("2", fd);
	}
	if (n >= 10)
	{
		ft_putnbr_fd((n / 10), fd);
		ft_putnbr_fd((n % 10), fd);
	}
	else
		ft_putchar_fd((n + '0'), fd);
}
